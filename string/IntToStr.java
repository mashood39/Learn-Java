public class IntToStr {
    public static void main(String[] args) {
        System.out.println("Integer to string");

        int num = 123;

        String num1 = String.valueOf(num);
        System.out.println(num1  + 2);

        String num2 = "3";
        System.out.println(num2 + 1);

        String str = num + "";
        System.out.println(str);

    }
}
